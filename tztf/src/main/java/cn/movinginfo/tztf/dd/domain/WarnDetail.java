package cn.movinginfo.tztf.dd.domain;

import java.text.SimpleDateFormat;
import java.util.Date;
import java.util.List;

public class WarnDetail {
	private Long id ;
	private String content;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getPubrange() {
		return pubrange;
	}
	public void setPubrange(String pubrange) {
		this.pubrange = pubrange;
	}
	public Date getPubdate() {
		return pubdate;
	}
	public void setPubdate(Date pubdate) {
		this.pubdate = pubdate;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getImagepath() {
		return imagepath;
	}
	public void setImagepath(String imagepath) {
		this.imagepath = imagepath;
	}
	public List<String> getChannelname() {
		return channelname;
	}
	public void setChannelname(List<String> channelname) {
		this.channelname = channelname;
	}
	SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm");
	private String title;
	private String pubrange;
	private Date pubdate;
	private String duration;
	private String imagepath;
	private List<String> channelname;
	private String date;
	public String getDate() {
		return sdf.format(pubdate);
	}

}








