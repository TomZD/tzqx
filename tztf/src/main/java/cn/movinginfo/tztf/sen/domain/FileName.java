package cn.movinginfo.tztf.sen.domain;

public class FileName {
	private String fileName;
	
	private String path;
	
	private String time;
	
	private String title;
	
	private String xmlPath;
	
	private String pointPath;
	
	public String getPointPath() {
		return pointPath;
	}

	public void setPointPath(String pointPath) {
		this.pointPath = pointPath;
	}

	public String getXmlPath() {
		return xmlPath;
	}

	public void setXmlPath(String xmlPath) {
		this.xmlPath = xmlPath;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	@Override
	public String toString() {
		return "FileName [fileName=" + fileName + ", path=" + path + ", time=" + time + "]";
	}
	
	

}
